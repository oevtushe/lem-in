#include "lem_in.h"

void	ft_free_parr(void ***arr)
{
	size_t i;

	i = 0;
	while ((*arr)[i])
		ft_memdel((void**)&(*arr)[i++]);
	ft_memdel((void**)arr);
	*arr = NULL;
}
